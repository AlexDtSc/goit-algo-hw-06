import networkx as nx
import matplotlib.pyplot as plt

graph = {
    'A': ['B', 'C'],
    'B': ['A', 'D', 'E'],
    'C': ['A', 'F', 'G'],
    'D': ['B', 'E'],
    'E': ['B', 'H'],
    'F': ['C', 'I', 'G'],
    'G': ['C'],
    'H': ['E'],
    'I': ['F']
}

G = nx.Graph(graph)
print(G)

