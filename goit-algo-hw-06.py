from collections import deque

def bfs_recursive(graph, queue, visited=None):
    # –ü–µ—Ä–µ–≤—ñ—Ä—è—î–º–æ, —á–∏ —ñ—Å–Ω—É—î –º–Ω–æ–∂–∏–Ω–∞ –≤—ñ–¥–≤—ñ–¥–∞–Ω–∏—Ö –≤–µ—Ä—à–∏–Ω, —è–∫—â–æ –Ω—ñ, —Ç–æ —ñ–Ω—ñ—Ü—ñ–∞–ª—ñ–∑—É—î–º–æ –Ω–æ–≤—É
    if visited is None:
        visited = set()
    # –Ø–∫—â–æ —á–µ—Ä–≥–∞ –ø–æ—Ä–æ–∂–Ω—è, –∑–∞–≤–µ—Ä—à—É—î–º–æ —Ä–µ–∫—É—Ä—Å—ñ—é
    if not queue:
        return
    # –í–∏–ª—É—á–∞—î–º–æ –≤–µ—Ä—à–∏–Ω—É –∑ –ø–æ—á–∞—Ç–∫—É —á–µ—Ä–≥–∏
    vertex = queue.popleft()
    # –ü–µ—Ä–µ–≤—ñ—Ä—è—î–º–æ, —á–∏ –≤—ñ–¥–≤—ñ–¥—É–≤–∞–ª–∏ —Ä–∞–Ω—ñ—à–µ –¥–∞–Ω—É –≤–µ—Ä—à–∏–Ω—É
    if vertex not in visited:
        # –Ø–∫—â–æ –Ω–µ –≤—ñ–¥–≤—ñ–¥—É–≤–∞–ª–∏, –¥—Ä—É–∫—É—î–º–æ –≤–µ—Ä—à–∏–Ω—É
        print(vertex, end=" ")
        # –î–æ–¥–∞—î–º–æ –≤–µ—Ä—à–∏–Ω—É –¥–æ –º–Ω–æ–∂–∏–Ω–∏ –≤—ñ–¥–≤—ñ–¥–∞–Ω–∏—Ö –≤–µ—Ä—à–∏–Ω.
        visited.add(vertex)
        # –î–æ–¥–∞—î–º–æ –Ω–µ–≤—ñ–¥–≤—ñ–¥–∞–Ω–∏—Ö —Å—É—Å—ñ–¥—ñ–≤ –¥–∞–Ω–æ—ó –≤–µ—Ä—à–∏–Ω–∏ –≤ –∫—ñ–Ω–µ—Ü—å —á–µ—Ä–≥–∏.
        queue.extend(set(graph[vertex]) - visited)
    # –†–µ–∫—É—Ä—Å–∏–≤–Ω–∏–π –≤–∏–∫–ª–∏–∫ —Ñ—É–Ω–∫—Ü—ñ—ó –∑ —Ç—ñ—î—é –∂ —á–µ—Ä–≥–æ—é —Ç–∞ –º–Ω–æ–∂–∏–Ω–æ—é –≤—ñ–¥–≤—ñ–¥–∞–Ω–∏—Ö –≤–µ—Ä—à–∏–Ω
    bfs_recursive(graph, queue, visited)


# –ó–∞–ø—É—Å–∫ —Ä–µ–∫—É—Ä—Å–∏–≤–Ω–æ–≥–æ –∞–ª–≥–æ—Ä–∏—Ç–º—É BFS
print(f'BFS_tree search way')  # –≤–∏–≤–µ–¥–µ —Ä–µ–±—Ä–∞ BFS-–¥–µ—Ä–µ–≤–∞ –∑ –∫–æ—Ä–µ–Ω–µ–º —É –≤—É–∑–ª—ñ A
