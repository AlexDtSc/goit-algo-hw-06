''' 
–ê–±–æ –º–æ–∂–Ω–∞ —Ç–∞–∫–∏–º —á–∏–Ω–æ–º –ø–µ—Ä–µ–ø–∏—Å–∞—Ç–∏ –Ω–∞—à –ø–æ–ø–µ—Ä–µ–¥–Ω—ñ–π –≥—Ä–∞—Ñ G –≤—Ä—É—á–Ω—É, –∑ –¥–æ–¥–∞–Ω–∏–º–∏ –≤–∞–≥–∞–º–∏

graph_with_weights = {                            
    'A': {'B': 1, 'C': 2},
    'B': {'A': 1, 'D': 3, 'E': 4},
    'C': {'A': 2, 'F': 5, 'G': 1},
    'D': {'B': 3, 'E': 2},
    'E': {'B': 4, 'H': 3},
    'F': {'C': 5, 'I': 4, 'G': 1},
    'G': {'C': 1, 'F': 1},
    'H': {'E': 3},
    'I': {'F': 4}
}
'''

# –ó–∞—Å—Ç–æ—Å—É–≤–∞–Ω–Ω—è –∞–ª–≥–æ—Ä–∏—Ç–º—É –î–µ–π–∫—Å—Ç—Ä–∏
def dijkstra(graph, start):
    # –Ü–Ω—ñ—Ü—ñ–∞–ª—ñ–∑–∞—Ü—ñ—è –≤—ñ–¥—Å—Ç–∞–Ω–µ–π —Ç–∞ –º–Ω–æ–∂–∏–Ω–∏ –Ω–µ–≤—ñ–¥–≤—ñ–¥–∞–Ω–∏—Ö –≤–µ—Ä—à–∏–Ω
    distances = {vertex: float('infinity') for vertex in graph}
    distances[start] = 0
    unvisited = list(graph.keys())

    while unvisited:
        # –ó–Ω–∞—Ö–æ–¥–∂–µ–Ω–Ω—è –≤–µ—Ä—à–∏–Ω–∏ –∑ –Ω–∞–π–º–µ–Ω—à–æ—é –≤—ñ–¥—Å—Ç–∞–Ω–Ω—é —Å–µ—Ä–µ–¥ –Ω–µ–≤—ñ–¥–≤—ñ–¥–∞–Ω–∏—Ö
        current_vertex = min(unvisited, key=lambda vertex: distances[vertex])

        # –Ø–∫—â–æ –ø–æ—Ç–æ—á–Ω–∞ –≤—ñ–¥—Å—Ç–∞–Ω—å —î –Ω–µ—Å–∫—ñ–Ω—á–µ–Ω–Ω—ñ—Å—Ç—é, —Ç–æ –º–∏ –∑–∞–≤–µ—Ä—à–∏–ª–∏ —Ä–æ–±–æ—Ç—É
        if distances[current_vertex] == float('infinity'):
            break

        for neighbor, weight in graph[current_vertex].items():
            distance = distances[current_vertex] + weight

            # –Ø–∫—â–æ –Ω–æ–≤–∞ –≤—ñ–¥—Å—Ç–∞–Ω—å –∫–æ—Ä–æ—Ç—à–∞, —Ç–æ –æ–Ω–æ–≤–ª—é—î–º–æ –Ω–∞–π–∫–æ—Ä–æ—Ç—à–∏–π —à–ª—è—Ö
            if distance < distances[neighbor]:
                distances[neighbor] = distance

        # –í–∏–¥–∞–ª—è—î–º–æ –ø–æ—Ç–æ—á–Ω—É –≤–µ—Ä—à–∏–Ω—É –∑ –º–Ω–æ–∂–∏–Ω–∏ –Ω–µ–≤—ñ–¥–≤—ñ–¥–∞–Ω–∏—Ö
        unvisited.remove(current_vertex)

    return distances


# –í–∏–∫–ª–∏–∫ —Ñ—É–Ω–∫—Ü—ñ—ó –¥–ª—è –≤–µ—Ä—à–∏–Ω–∏ A
dijkstra_shortest_paths = dijkstra(graph_with_weights, 'A') 
