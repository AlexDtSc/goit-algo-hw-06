# ‚ùå –ü–æ—Ç–æ—á–Ω–∞ —Å—Ç—Ä—É–∫—Ç—É—Ä–∞: {'A': ['B', 'C']} ‚Äî –Ω–µ –ø—ñ–¥—Ö–æ–¥–∏—Ç—å
