## DFS
def dfs_recursive(graph, vertex, visited=None):
    if visited is None:
        visited = set()
    visited.add(vertex)
    print(vertex, end=' ')  # –í—ñ–¥–≤—ñ–¥—É—î–º–æ –≤–µ—Ä—à–∏–Ω—É
    for neighbor in graph[vertex]:
        if neighbor not in visited:
            dfs_recursive(graph, neighbor, visited)

